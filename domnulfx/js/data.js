function getAge(year, month, day) {
    var today = new Date();
    var birthDay = new Date(year, month - 1, day);
    var age = today.getFullYear() - birthDay.getFullYear();
    var m = today.getMonth() - birthDay.getMonth();
    if (m < 0 || (m === 0 && today.getDate() < birthDay.getDate()))
        age--;
    return age;
}